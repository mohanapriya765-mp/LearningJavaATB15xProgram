package ex_10_For_Loop;

public class Lab075_For_If_Loop {
    public static void main(String[] args) {
        for(int i=0; i<18; i++) {
            if (i>15) {
                System.out.println("You got gift");
            }
            else {
                System.out.println("You don't get gift");
            }
        }
    }
}
