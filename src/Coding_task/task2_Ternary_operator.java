package Coding_task;

public class task2_Ternary_operator {
    public static void main(String[] args) {
        int m1 = 40;
        String r1 = (m1 >= 40) ? "PASS" : "FAIL";
        System.out.println(r1);

        int m2 = 30;
        String r2 = (m2 >= 40) ? "PASS" : "FAIL";
        System.out.println(r2);

        int m3 = 70;
        String r3 = (m3 >= 40) ? "PASS" : "FAIL";
        System.out.println(r3);
    }
}
