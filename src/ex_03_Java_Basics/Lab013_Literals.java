package ex_03_Java_Basics;

public class Lab013_Literals {
    public static void main(String[] args) {
        // data_type variable_name(identifier) =  variable_value (Literal);
        // Values are literal
        // Types of literal - 3
        // All datatypes have their own literal

        //char literals
        char c = 'A';
        char c1 = '@';
        char c2 = '#', c3 = '%';

        char new_line = '\n';
        char tab_line = '\t';
        char back_space = '\b';
        char carriage_return = '\r';
        System.out.println("Mohana \n Priya");
        System.out.println("Mohana \t Priya");
        System.out.println("Mohana \b Priya");
        System.out.println("Mohana \r Priya");
    }
}
